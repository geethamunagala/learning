#include <stdio.h>
int main()
{
    int a,b;
    printf("enter a value:");
    scanf("%d",&a);
    printf("enter b value:");
    scanf("%d",&b);
    printf("value of a+b=%d\n",a+b);
    printf("value of a-b=%d\n",a-b);
    printf("value of a*b=%d\n",a*b);
    printf("value of a/b=%d\n",a/b);
    return 0;
}